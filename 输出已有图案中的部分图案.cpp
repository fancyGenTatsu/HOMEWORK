#include<stdio.h>
int main()
{
	char s[6][9]={{'-','-','*','*','-','*','*','-','-'},
	             {'-','*','-','-','*','-','-','*','-'},
	             {'-','*','-','-','-','-','-','*','-'},
				 {'-','-','*','-','-','-','*','-','-'},
	             {'-','-','-','*','-','*','-','-','-'},
				 {'-','-','-','-','*','-','-','-','-'}};
	int x,y,w,h,i,j;
	scanf("%d%d%d%d",&x,&y,&w,&h);
	if(1<=x&&x<=9&&1<=y&&y<=6&&1<=w&&w<=9&&1<=h&&h<=6&&2<=(x+w)&&(x+w)<=10&&2<=(y+h)&&(y+h)<=7)
	
	{
		for(i=(y-1);i<=(h+y-2);i++)
		{
			for(j=(x-1);j<=(w+x-2);j++)
			{
				printf("%c",s[i][j]);
			}
		printf("\n");
		}
	}
	
	else
	printf("Input Error");
	return 0;
}
